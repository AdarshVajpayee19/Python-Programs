# Arithmetic Operators
print("Arithmetict Operators(+, -, *, /, %, //, **):")
print("5 + 6 is = ", 5 + 6)
print("5 - 6 is = ", 5 - 6)
print("5 * 6 is = ", 5 * 6)
print("5 / 6 is = ", 5 / 6)
print("5 % 6 is = ", 5 % 6)
print("5 // 6 is = ", 5 // 6)  # Round Kar ke Dek Value (Floor Division Operator).
print("2 ** 6 is = ", 2 ** 6,"\n")

# Assignment Operators
print("Assignment Operators(=, +=, -=, %=, *=, /=): ")
x = 5
print(x)
x += 7
print(x)
x -= 5
print(x)
x /= 5
print(x)
x %= 5
print(x,"\n")

# Comparison Operators
print("Comparison Operators(!=, >, >=, <, <=, ==):")
i = 5
print(i != 5)
print(i > 5)
print(i >= 5)
print(i == 5)
print(i < 5)
print(i <= 5)

# Logical Operators
print("Logical Operators (and, or, not): ")
a = True
b = False

print(a and b)
print(a and a)
print(a or b)
print(not a)
print(not b)

# Identity Operators
print("Identity Operators(is, is not): ")
print(5 is 6)
print(5 is not 6)

# Membership Operators
print("Membership Operators (in ,not in) :")
list = [3, 32, 33, 5, 66, 69, 100]
print(32 in list)
print(324 not in list)

# Bitwise Operators
print("Bitwise Operators (&, ^, |, etc.....):")
print(0 | 3)
print(0 & 2)

