mystr = "My name is Adarsh vajpayee"

# Some print Statements and String  Slicing Concept
print(mystr)
print(mystr[4])
print(mystr[11:])  # string slicing
print(len(mystr))
print(mystr.upper())
print(mystr[0:26:3])  # by Skipping 2 characters by giving 3 as exclusive.
print(mystr[11::2])
print(mystr[::2])
print(mystr[::])
print(mystr[-4:-2])  # negative silicng
print(mystr[::-1])  # Reversed String................................
print(mystr[::-2])

